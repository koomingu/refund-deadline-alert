function calcPrice(scores, origin, destination) {
  const a = scores[origin];
  const b = scores[destination];
  if (a == null || b == null) return null;
  const diff = Math.abs(a - b);
  if (diff === 0) return null;
  // 100원 단위 반올림
  return Math.round(diff / 100) * 100;
}

export function estimatePrice(vendorType, origin, destination) {
  if (!origin || !destination || origin === destination) return null;
  if (vendorType === 'srt') return calcPrice(SRT_SCORES, origin, destination);
  if (vendorType === 'ktx') return calcPrice(KTX_SCORES, origin, destination);
  return null; // 버스는 너무 다양해서 제외
}
